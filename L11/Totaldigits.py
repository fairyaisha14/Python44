num = int(input("Write down any number: "))
while num < 10:
    print ("This number has 1 digit")
while num < 100:
    print ("This number has 2 digits")
while num < 1000:
    print ("This number has 3 digits")
while num < 10000:
    print ("This number has 4 digits")
while num < 100000:
    print ("This number has 5 digits")
while num < 1000000:
    print ("This number has 6 digits")
while num < 10000000:
    print ("This number has 7 digits")
else:
    print ("This numbers has more than 7 digits")
